//! Future Basics - Learning the foundations of async Rust
//!
//! This crate provides educational examples and implementations
//! for understanding the core concepts of async programming in Rust.
//!
//! Run the examples to see the concepts in action:
//!
//! ```bash
//! cargo run --example 01_simple_future
//! cargo run --example 02_timer_future
//! cargo run --example 03_manual_join
//! cargo run --example 04_state_machine
//! cargo run --example 05_yield_now
//! ```
